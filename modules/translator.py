'''
order:
[Bit 7][Bit 5]
[Bit 6][Bit 4]
[Bit 3][Bit 2]
[Bit 1][Bit 0]
'''

class Braille_translator():
    def __init__(self):
        self.braille_dict = {
            # Lowercase letters
            'a': 0b10000000, 'b': 0b11000000, 'c': 0b10100000, 'd': 0b10110000, 'e': 0b10010000, 'f': 0b11100000,
            'g': 0b11110000, 'h': 0b11010000, 'i': 0b01100000, 'j': 0b01110000, 'k': 0b10001000, 'l': 0b11001000,
            'm': 0b10101000, 'n': 0b10111000, 'o': 0b10011000, 'p': 0b11101000, 'q': 0b11111000, 'r': 0b11011000,
            's': 0b01101000, 't': 0b01111000, 'u': 0b10001100, 'v': 0b11001100, 'w': 0b01110100, 'x': 0b10101100,
            'y': 0b10111100, 'z': 0b10011100,

            # Uppercase letters (capitalized by adding the 7th dot)
            'A': 0b10000010, 'B': 0b11000010, 'C': 0b10100010, 'D': 0b10110010, 'E': 0b10010010, 'F': 0b11100010,
            'G': 0b11110010, 'H': 0b11010010, 'I': 0b01100010, 'J': 0b01110010, 'K': 0b10001010, 'L': 0b11001010,
            'M': 0b10101010, 'N': 0b10111010, 'O': 0b10011010, 'P': 0b11101010, 'Q': 0b11111010, 'R': 0b11011010,
            'S': 0b01101010, 'T': 0b01111010, 'U': 0b10001110, 'V': 0b11001110, 'W': 0b01110110, 'X': 0b10101110,
            'Y': 0b10111110, 'Z': 0b10011110,

            # Accented letters (lowercase)
            'á': 0b10100000, 'é': 0b10010000, 'í': 0b01100000, 'ó': 0b10011000, 'ú': 0b10001100, 'ñ': 0b11111000,
            
            # Accented letters (uppercase)
            'Á': 0b10100010, 'É': 0b10010010, 'Í': 0b01100010, 'Ó': 0b10011010, 'Ú': 0b10001110, 'Ñ': 0b11111010,

            # Letters with diaeresis (lowercase)
            'ü': 0b11001100,
            
            # Letters with diaeresis (uppercase)
            'Ü': 0b11001110,
            
            # Numbers encoded directly in the 8-dot system
            '1': 0b10000001, '2': 0b11000001, '3': 0b10100001, '4': 0b10110001, '5': 0b10010001, '6': 0b11100001,
            '7': 0b11110001, '8': 0b11010001, '9': 0b01100001, '0': 0b01110001,
            
            # Punctuation symbols
            '.': 0b00001000, ',': 0b01000000, ';': 0b01100000, ':': 0b01010000, '!': 0b01110000, '¿': 0b00111000,
            '¡': 0b01111000, '?': 0b01011000, '(': 0b10010100, ')': 0b10010100, '-': 0b00100000, '"': 0b01010100,
            "'": 0b01000000, '/': 0b10000100, '@': 0b11000100, '&': 0b11100100, '%': 0b01111000, '+': 0b10001100,
            '*': 0b10101100,
            
            # Whitespace
            ' ': 0b00000000
        }


    def translate(self, key):
        return self.braille_dict[key]
